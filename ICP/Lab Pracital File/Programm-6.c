#include<stdio.h>

int main()
{
	int a,b;
	
	printf("Enter The First Value:-");
	scanf("%d",&a);
	
	printf("Enter The Second Value:-");
	scanf("%d",&b);
	
	printf("Addition Is %d\n",a+b);
	printf("Substraction Is %d\n",a-b);
	printf("Multiplication Is %d\n",a*b);
	printf("Division Is %d\n",a/b);
	printf("Modulo Is %d",a%b);
	
	return 0;
	
}