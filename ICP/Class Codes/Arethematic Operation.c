#include<stdio.h>
int main(void){
	int num1,num2;
	printf("Enter The First Number:-");
	scanf("%d",&num1);
	printf("Enter The Second Number:-");
	scanf("%d",&num2);
	printf("Addition Is %d\n",num1+num2);
	printf("Substraction Is %d\n",num1-num2);
	printf("Multiplication Is %d\n",num1*num2);
	printf("Division Is %d\n",num1/num2);
	return 0;
}